    //Function Load Table Admin
    private void loadAdminTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Admin Code");
        model.addColumn("Admin Name");
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Admin Email");
        model.addColumn("Admin Phone");
        
        try {
            int no=1;
            sql = "SELECT * FROM admin";
            Koneksi DB = new Koneksi();
            DB.config();
            con = DB.con;
            stat = DB.stm;
            rs = stat.executeQuery(sql);
            while(rs.next()){
                model.addRow(new Object[]{no++,rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)}
                );
            }
            tblAdmin.setModel(model);
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
