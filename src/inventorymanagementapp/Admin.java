    private void btnAdminAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminAddActionPerformed
        String adminName = txtAdminName.getText();
        String adminUsername = txtAdminUsername.getText();
        String adminPassword = txtAdminPassword.getText();
        String adminEmail = txtAdminEmail.getText();
        String adminPhone = txtAdminPhone.getText();

        try {
            Koneksi DB = new Koneksi();
            DB.config();
            con = DB.con;
            stat = DB.stm;
            sql = "INSERT INTO admin (admin_name,username,password,admin_email,admin_phone) VALUES ('"+adminName+"',"
            + "'"+adminUsername+"',"
            + "'"+adminPassword+"',"
            + "'"+adminEmail+"',"
            + "'"+adminPhone+"')";
            stat.execute(sql);
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        } finally {
            loadAdminTable();
            clearAdmin();
        }
    }//GEN-LAST:event_btnAdminAddActionPerformed

