    private void btnSupplierAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupplierAddActionPerformed
        String supplierName = txtSupplierName.getText();
        String supplierAddress = txtSupplierAddress.getText();
        String supplierEmail = txtSupplierEmail.getText();
        String supplierPhone = txtSupplierPhone.getText();
        
        try {
            Koneksi DB = new Koneksi();
            DB.config();
            con = DB.con;
            stat = DB.stm;
            sql = "INSERT INTO supplier (supplier_name,supplier_address,supplier_email,supplier_phone) VALUES ('"+supplierName+"',"
                    + "'"+supplierAddress+"',"
                    + "'"+supplierEmail+"',"
                    + "'"+supplierPhone+"')";
                stat.execute(sql);
                JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
                con.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            } finally {
            loadSupplierTable();
            clearSupplier();
        }
    }//GEN-LAST:event_btnSupplierAddActionPerformed

