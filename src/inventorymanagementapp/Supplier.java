    //Function Load Table Supplier
    private void loadSupplierTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Supplier Code");
        model.addColumn("Supplier Name");
        model.addColumn("Supplier Address");
        model.addColumn("Supplier Email");
        model.addColumn("Supplier Phone");
        
        try {
            int no=1;
            sql = "SELECT * FROM supplier";
            Koneksi DB = new Koneksi();
            DB.config();
            con = DB.con;
            stat = DB.stm;
            rs = stat.executeQuery(sql);
            while(rs.next()){
                model.addRow(new Object[]{no++,rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)}
                );
            }
            tblSupplier.setModel(model);
            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
